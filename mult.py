# defines the mult method

def mult(a, b):
    """
    Multiplies a and b.

    >>> a, b = 2, 5
    >>> mult(a, b)
    10

    # Using traceback
    >>> mult(1, 2, 3)
    Traceback (most recent call last):
    TypeError: mult() takes 2 positional arguments but 3 were given
    """

    return a * b
